package ch06.exercise.p16;

public class Printer {
    void println(int p) {
        System.out.println(p);
    }

    void println(double p) {
        System.out.println(p);

    }

    void println(String p) {
        System.out.println(p);

    }

    void println(boolean p) {
        System.out.println(p);

    }
}
